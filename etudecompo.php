                        <h2>Le Go un langage performant</h2>

                        Le langage de programmation Go est performant et il rend le travail d'un développeur plus facile de plusieurs façons:<br>
                        - La commodité c'est à dire que le Go est parfois comparé à un langage de script comme Python car il répond à énormement de besoins de programmation.
                        Certaines fonctionnalités sont intégrées dans le langage lui-même, telles que les routines de programmation distribuées et le multithreading (Capable de faire plusieurs tâches simultanément), et d'autres modules complémentaires se trouvent dans les bibliothèques Google par défaut, telles que le paquet http de Go.
                        Comme Python, Go fournit automatiquement la gestion de la mémoire.
                        Go est compilé en binaire natif. Et contrairement à C ou C ++, Go compile si rapidement qu'il ressemble plus à un langage de script qu'à un langage de compilé.<br>
                        - La vitesse Les binaires Go fonctionnent moins vite que le code C, mais la différence est négligeable pour la plupart des applications.
                        Pour la plupart des projets, la performance de Go est aussi pratique que C, et elle est généralement plus rapide que les autres langages connus pour un développement fluide, comme JavaScript, Python et Ruby.<br>
                        - La portabilité Les exécutables configurés avec la toolchain Go peuvent fonctionner seuls, sans dépendance externe. La toolchain Go est disponible pour une large gamme de systèmes d'exploitation et de plateformes matérielles et peut être utilisée pour créer des binaires fonctionnant sur différentes plateformes.<br>
                        - L'intéropérabilité Les programmes Go peuvent communiquer avec des bibliothèques C externes ou créer des appels systèmes natifs.<br>
                        - La Documentation Go excelle vraiment quand il s'agit de documenter votre propre code. Il existe un moyen standard de documenter toutes vos fonctions et bibliothèques, ainsi que d'utiliser les bons outils au bon moment.
                        Vous obtenez même des avertissements lorsque vous écrivez des fonctions sans documentation.
